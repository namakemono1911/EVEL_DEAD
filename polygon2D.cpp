//////////////////////////////////////////////////////////////////////////////
// ヘッダーインクルード
//////////////////////////////////////////////////////////////////////////////
#include "main.h"
#include "scene.h"
#include "scene2D.h"
#include "polygon2D.h"

//////////////////////////////////////////////////////////////////////////////
// 定数定義
//////////////////////////////////////////////////////////////////////////////
#define TEX_NAME	"data/texture/img.jpg"

//////////////////////////////////////////////////////////////////////////////
//関数名	: Polygon2D::init()
//返り値	: 
//説明		: 初期化
//////////////////////////////////////////////////////////////////////////////
HRESULT	Polygon2D::init	( D3DXVECTOR3 pos, D3DXVECTOR3 size, D3DXVECTOR2 texPos, D3DXVECTOR2 texSize )
{
	setAll(pos, size, texPos, texSize);
	loadTex(TEX_NAME);
	Scene2D::init();

	return S_OK;
}

//////////////////////////////////////////////////////////////////////////////
//関数名	: Polygon2D::uninit()
//返り値	: 
//説明		: 終了
//////////////////////////////////////////////////////////////////////////////
void	Polygon2D::uninit	( void )
{
	Scene2D::uninit();
}

//////////////////////////////////////////////////////////////////////////////
//関数名	: Polygon2D::update()
//返り値	: 
//説明		: 更新
//////////////////////////////////////////////////////////////////////////////
void	Polygon2D::update	( void )
{
	Scene2D::update();
}

//////////////////////////////////////////////////////////////////////////////
//関数名	: Polygon2D::draw()
//返り値	: 
//説明		: 描画
//////////////////////////////////////////////////////////////////////////////
void	Polygon2D::draw	( void )
{
	Scene2D::draw();
}

//////////////////////////////////////////////////////////////////////////////
//関数名	: Polygon2D::create()
//返り値	: 
//説明		: 生成
//////////////////////////////////////////////////////////////////////////////
Polygon2D	*Polygon2D::create	( D3DXVECTOR3 pos, D3DXVECTOR3 size, D3DXVECTOR2 texPos, D3DXVECTOR2 texSize )
{
	//変数宣言
	Polygon2D	*polygon;

	//生成
	polygon = new Polygon2D(PRIME_FORTH);

	//初期化
	polygon->init(pos, size, texPos, texSize);

	return polygon;
}